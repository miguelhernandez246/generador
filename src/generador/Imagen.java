package generador;

public class Imagen {
	public String image;
	public float x_size;
	public float y_size;
	public float x_pos;
	public float y_pos;
	
	public Imagen(String image,float x_size,float y_size,float x_pos,float y_pos) {
		this.image = image;
		this.x_size = x_size;
		this.y_size = y_size;
		this.x_pos = x_pos;
		this.y_pos = y_pos;
	}
}
